package es.sergioangelverbo.model.entity;

/**
 * Filenames for tube images
 * @author Sergio
 *
 */
public class TubeImages {

	private String top, body;
	
	public TubeImages(String top, String body) {
		this.top = top;
		this.body = body;
	}
	
	public String getTop() { return top; }
	public String getBody() { return body; }
	
}
